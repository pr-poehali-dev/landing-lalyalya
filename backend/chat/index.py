def clean_reply(text: str) -> str:
    text = re.sub(r'\[\d+\]', '', text)
    text = text.replace('**', '').replace('__', '')
    text = re.sub(r'(?m)^#{1,6}\s*', '', text)
    text = re.sub(r'[ \t]{2,}', ' ', text)
    return text.strip()

